#from keras.utils.visualize_util import plot


def _shortcut(input, residual):
    return merge([input, residual], mode='sum')


def _bn_relu_conv(nb_filter, nb_row, nb_col, subsample=(1, 1), bn=False):
    def f(input):
        if bn:
            input = BatchNormalization(mode=0, axis=1)(input)
        activation = Activation('relu')(input)
        return Convolution2D(nb_filter=nb_filter, nb_row=nb_row, nb_col=nb_col, subsample=subsample, border_mode="same")(activation)
    return f


def _residual_unit(nb_filter, init_subsample=(1, 1)):
    def f(input):
        residual = _bn_relu_conv(nb_filter, 3, 3)(input)
        residual = _bn_relu_conv(nb_filter, 3, 3)(residual)
        return _shortcut(input, residual)
    return f


def ResUnits(residual_unit, nb_filter, repetations=1):
    def f(input):
        for i in range(repetations):
            init_subsample = (1, 1)
            input = residual_unit(nb_filter=nb_filter,
                                  init_subsample=init_subsample)(input)
        return input
    return f


def stresnet(c_conf=(4, 1, 35, 12), p_conf=(4, 1, 35, 12), t_conf=(3,1,35,12),external_dim=9, nb_residual_unit=4):
    '''   针对时空数据定义时空残差网络
    model.fit会按照输入层适配进去，根据输入维度去配置放入

    C - Temporal Closeness
    P - Period
    T - Trend
    conf = (len_seq, nb_flow, map_height, map_width)
    external_dim
    '''
    # main input
    main_inputs = []
    outputs = []
    ####################################针对于
    for conf in [c_conf, p_conf,t_conf]:
        if conf is not None:
            #因为conf是四维的，所以这些东西都可以对应放置进去， 把序列长度、流入流出量、图的高宽都放进去
            #这里因为是一个个添加的，所以会将所有的输入序列全放进去，shape是所代表的形状，提前制定好了形状
            len_seq, nb_flow, map_height, map_width = conf
            input = Input(shape=(nb_flow * len_seq, map_height, map_width))
            main_inputs.append(input)
            # Conv1    定义好卷积层  顺带后面添加了残差单元


            conv1 = Convolution2D(
                nb_filter=64, nb_row=3, nb_col=3, border_mode="same")(input)
            #conv1=Dropout(0.2)(conv1)
            # [nb_residual_unit] Residual Units
            residual_output = ResUnits(_residual_unit, nb_filter=64,
                              repetations=nb_residual_unit)(conv1)
            # Conv2 第一第二个卷积层
            activation = Activation('relu')(residual_output)
            #conv1=Dropout(0.2)(activation)
            conv2 = Convolution2D(
                nb_filter=50, nb_row=3, nb_col=3, border_mode="same")(activation)

            #-------------------    为了预测出35*3大小的图，使用下面的后跟全链接进行转换  尺寸的方案，否则 经过卷积后，是没法变成想要的维度的    -----------------------#
            '''  下面是我的构思方案，   让最后的输出是35*12纬度的，主要是使用全连接的reshape方式不能用，所以我改了下卷积，。。
            '''

            embedding = Activation('relu')(conv2)

            conv3 = Convolution2D(
                nb_filter=50, nb_row=1, nb_col=10)(embedding)


            # ------------------------------------------------------------------------------------------------------------------------------------------#
            outputs.append(conv3)


    # parameter-matrix-based fusion
    if len(outputs) == 1:
        main_output = outputs[0]
    else:
        from deepst.models.iLayer import iLayer
        new_outputs = []
        for output in outputs:
            new_outputs.append(iLayer()(output))
        #定义融合模式
        main_output = merge(new_outputs, mode='sum')

    # fusing with external component     现在考虑对额外因素信息的引入
    if external_dim != None and external_dim > 0:
        # external input   将外部因素融入时，定义了一个全连接层，并进行了维度的转换
        external_input = Input(shape=(external_dim,))
        main_inputs.append(external_input)
        embedding = Dense(output_dim=10)(external_input)
        embedding = Activation('relu')(embedding)
        h1 = Dense(output_dim=50 * map_height *3)(embedding)
        activation = Activation('relu')(h1)
        external_output = Reshape((50, map_height, 3))(activation)
        #最终的输出是通过主融合输出和外部因素的输出的  加和在一起的
        main_output = merge([main_output, external_output], mode='sum')
    else:
        print('external_dim:', external_dim)

    #----------  融入第四层输入  将预测天气融入其中 ----------#

    # pred_input = Input(shape=(50,35,6))
    # main_inputs.append(pred_input)
    # conv3 = Convolution2D(
    #    nb_filter=64, nb_row=3, nb_col=3, border_mode="same")(pred_input)
    # #embedding1 = Dense(output_dim=10500)(pred_input)
    # embedding1 = Activation('relu')(conv3)
    # conv4 = Convolution2D(
    #     nb_filter=50, nb_row=3, nb_col=3, border_mode="same")(embedding1)
    # embedding2 = Activation('relu')(conv4)
    # conv5 = Convolution2D(
    #     nb_filter=50, nb_row=1, nb_col=4)(embedding2)
    #
    # # 最终的输出是通过主融合输出和外部因素的输出的  加和在一起的
    # main_output = merge([main_output, conv5], mode='sum')

    #----------------------------------------------------#
    main_output = Activation('tanh')(main_output)
    model = Model(input=main_inputs, output=main_output)

    return model

